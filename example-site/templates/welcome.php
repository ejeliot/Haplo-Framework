<h1>Welcome to the Haplo Framework</h1>
<p>A fast, lightweight and secure PHP 5 MVC framework.</p>
<h2>Features</h2>
<ul>
    <li>A regular expressions based URL router</li>
    <li>PHP templating</li>
    <li>A translations library</li>
    <li>Caching (file based or memcached)</li>
    <li>Automatic protection against cross site scripting (XSS)</li>
    <li>CSRF protection with &quot;nonce&quot; library</li>
    <li>Page timing functionality</li>
</ul>
<h2>Requirements</h2>
<ul>
    <li>PHP 5.3</li>
    <li>mod rewrite for friendly URLs (without /index.php/)</li>
</ul>